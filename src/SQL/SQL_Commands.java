package SQL;

public class SQL_Commands {
    /*
    DML  - команды, работают с внутренностью таблиц на прямую
    (Data Manipulation Language)
    SELECT - делает выборку из таблиц
    INSERT - вставлять (добавлять строки)
    UPDATE - изменять данные в таблице
    DELETE - удаляет строкув таблице
    MERGE - объеденяет INSERT, UPDATE, DELETE

    DDL - команды не работают на прямую, работают со структурой таблицы
    (Data definition Language)
    CREATE - создание таблицы
    ALTER - изменение структуры таблицы (добавление столбцов)
    DROP - удаляет объекты (таблицу)
    RENAME - переименовать столбец
    TRUNCATE - очищается таблица

    TSL - контроль транзакций
    (Transaction Control Language)
    COMMIT - подтвердить
    ROLLBACK - откатить
    SAVEPOINT - точка сохранения

    DCL -
    (Data Control Language)
    GRANT - дать права
    REVOKE - забрать права
     */
}
