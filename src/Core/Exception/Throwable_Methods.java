package Core.Exception;

public class Throwable_Methods {
    /*
    №

Метод и описание

1 public String getMessage()
Возврат подробного сообщения о произошедшем исключении.
Инициализация данного сообщения производится в конструкторе Throwable.

2 public Throwable getCause()
Возврат причины исключения, представленной объектом Throwable.

3 public String toString()
Возврат имени класса, соединенного с результатом getMessage().

4 public void printStackTrace()
Выведение результата toString() совместно с трассировкой стека в System.err, поток вывода ошибок.

5 public StackTraceElement [] getStackTrace()
Возврат массива, содержащего каждый элемент в трассировке стека.
Элемент с номером 0 представляет вершину стека вызовов, последний элемент массива отображает метод на дне стека вызовов.

6 public Throwable fillInStackTrace()
Заполняет трассировку стека данного объекта Throwable текущей трассировкой стека,
дополняя какую-либо предшествующую информацию в трассировке стека.
     */
}
