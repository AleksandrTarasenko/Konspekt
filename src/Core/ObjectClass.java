package Core;

public class ObjectClass {
    /*
    все классы наследуются от класса Object
    все типы и классы могут реализовать те методы, которые определены в классе Object.

    Метод toString
    Метод toString служит для получения представления этого объекта в виде строки.

    Person tom = new Person("Tom");
        System.out.println(tom.toString()); // Будет выводить что-то наподобие Person@7960847b
        Если переопределить метод toString, то получим строковое значение\
        class Person {

    private String name;

    public Person(String name) {
        this.name = name;
    }

    @Override // переопределяем метод toString
    public String toString() {
        return "Person " + name;
    }
}

    Метод getClass
    позволяет получить тип этого объекта:
    Person tom = new Person("Tom");
    System.out.println(tom.getClass()); // class Person
     */
}
